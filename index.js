
function subtract(a,b) {
    return a-b
}

function multiply(a,b) {
    return a*b
}

function divide(a,b) {
    return a/b
}
function add(a,b) {
    return a+b
}

function increment(n){
    return n+1
}
function decrement(n){
    return n-1
}

function makeInt(n , a=10){
    return parseInt(n,a)
}
function preserveDecimal (n , a=10){
    return parseFloat(n,a)
}